    private void bMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bMostrarActionPerformed
        borrarTabla(tablaJugadores);
        try {
            DefaultTableModel modelo = new DefaultTableModel();
            ResultSet rs = Mostrar.getTabla("select * from datos");
            modelo.setColumnIdentifiers(new Object[]{"id", "nombre", "puntuacion"});
            try {
                while (rs.next()) {
                    modelo.addRow(new Object[]{rs.getInt("id"), rs.getString("nombre"), rs.getInt("puntuacion")});
                }
                tablaJugadores.setModel(modelo);
            } catch (Exception e) {
                System.out.println("Error al realizar la consulta");
            }
        } catch (Exception e) {
            System.out.println(e);
        }        // TODO add your handling code here:
    }//GEN-LAST:event_bMostrarActionPerformed

